package model.beans;

public class SubstanceAccumBean {
	
	private String srvUserId;
	private String substance;
	private String accum;
	private String updatedBy;
	private String updatedOn;
	
	public String getSubstance() {
		return substance;
	}
	public void setSubstance(String substance) {
		this.substance = substance;
	}
	
	public String getSrvUserId() {
		return srvUserId;
	}
	public void setSrvUserId(String srvUserId) {
		this.srvUserId = srvUserId;
	}
	public String getAccum() {
		return accum;
	}
	public void setAccum(String accum) {
		this.accum = accum;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getUpdatedOn() {
		return updatedOn;
	}
	public void setUpdatedOn(String updatedOn) {
		this.updatedOn = updatedOn;
	}


}
